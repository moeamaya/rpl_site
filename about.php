			                 <h5>What exactly do we do?</h5>
			    <h2>About the RPL</h2>
			    <p>The Architecture Department's Rapid Prototyping Lab provides equipment and software that students and faculty can use to fabricate physical objects from CAD models. 
			    	RPL facilities include laser cutters, 3D printers which creates three-dimensional models from CAD designs,CNC routers which carves wood or foam in three 
				dimensions under computer controls....among many other tools and services for students.</p>
			    <p>In addition to providing model-making facilities in support of design studios and other coursework, we are integrated into the MArch curriculum as a teaching resource, offering annual 
				introductory fabrication classes and periodic classes on advanced topices, while maintaining a classroom presence in many other related courses taught within the department.</p>
			    <h2>Policy</h2>
				<p><strong>Health and Safety:</strong> All work conducted in any facility managed by the Department of Architecture Rapid Prototyping Lab (RPL) must be conducted in accordance with this policy. 
				Safety rules are posted clearly in all our shop spaces and include standards for attire, alertness and state of mind, use of personal protective equipment, housekeeping, 
				training, and safe equipment use practices. Anyone, including students, staff, faculty, and visitors to the lab may be asked to leave and may have access revoked at any 
				time by RPL staff (including lab managers and student lab monitors) for being unable to conform to this policy.</p>

				<p><strong>Access:</strong> Access is permitted only for individuals employed by MIT or students receiving academic credit for the work being done in the shop. No shop access can be offered 
				to anyone not falling into one of these two categories. Generally, this access is reserved for members of the MIT Department of Architecture, although we do collaborate 
				on specific projects with other research groups on campus. We do not offer access to non-architecture students wishing to pursue their own work in our facilities.</p>

				<p>During the fall and spring semesters student academic work has first priority for access to equipment and other resources, with faculty and UROP research projects offered 
				use of facilities as available thereafter. During the summer, access is available only for faculty research projects and, in some cases, to students receiving academic credit 
				during the summer term. Access during IAP varies from year to year and is dependent on course offerings and availability of staff.</p>

				<p>All facilities and equipment may be taken offline from time to time for safety and maintenance reasons. We make every effort to correct issues in a timely fashion and will 
				send notice of planned outages as early as possible. In the event of unplanned repairs, RPL staff will discuss other techniques for finishing projects within our facilities but 
				we cannot offer or provide access to other shop facilities on the MIT campus, nor do we offer access to commercial services when our equipment is down.</p>

				<p><strong>Training and supervision:</strong> The Department of Architecture requires general shop safety training and hazardous waste disposal training for everyone who will enter any of our shop 
				spaces. Students using manually operated tools in a woodworking shop are required to undergo training on that equipment in one of the wood shop spaces. The MIT EHS Working Alone 
				Policy states that no one may operate a machine tool without a second person present, except in rare situations where the individual is given express prior approval from a shop manager. 
				To meet the requirements of this policy we provide staffing for our general shop spaces during hours they are available for student use during the fall and spring semesters. 
				Extension of these hours during peak periods may be possible with prior approval. The shop may not be staffed during the summer and research projects wishing to access the facilities must 
				hire students who already have the appropriate training. When staffing is unavailable and research projects need to access equipment covered under the EHS Working Alone Policy, those 
				projects will be responsible for providing both people.</p>

				<p>Additional training and supervision is required for certain equipment as follows:</p>

				<li><i>Laser cutters and thermoformer - self-serve upon completion of annual training - times will be announced at the beginning of each semester</i></li>
				<li><i>CNC routers, milling machine, lathe, waterjet, and robotic arm - operated under supervision and instruction of RPL staff except with express prior approval from RPL manager</i></li>
				<li><i>3D printing is offered as a service but may require minimal training on model post-processing</i></li>
				<li><i>Other desktop tools such as the Roland vinyl cutter, Modela mill, and ZCorp 3D scanner require initial instructional training but may be operated without additional supervision by individuals trained in their use</i></li>
				
				<p>Availability of staffing and schedule for machine access will be posted at the beginning of each semester.</p>
          
